<div class="maintext">

    <p><b>Підприємництво</b> У 1990—1991 роках почав працювати в приватному секторі, зокрема заступником
        генерального директора Об'єднання малих підприємств та підприємців «Республіка». Згодом очолив АТ
        «Біржовий
        дім „Україна“», яким керував до 1993 року.Здобувши вищу освіту, Порошенко розпочав власний бізнес з
        продажу
        какао-бобів. У 1990 роках він придбав
        кілька кондитерських підприємств. Потім об'єднав їх у групу «Рошен», що згодом стала найбільшим
        виробником
        кондитерських виробів в Україні. У групу входять фабрики й комбінати в Україні й Литві. Підприємства
        кондитерської промисловості принесли Порошенкові великі прибутки й прізвисько «шоколадний король» </p>
    <p>У 1993—1998 роках працював генеральним директором Українського промислово-інвестиційного концерну.</p>
    <p>Бізнес Порошенка також включає в себе «Укрпромінвест-Агро», телеканал «5 канал» і низку інших
        підприємств.
        Раніше важливою складовою бізнесу Порошенка була також автомобільна корпорація «Богдан», але 2009 року
        контрольну частку корпорації придбав його тодішній партнер Олег Свинарчук.Порошенкові також належать
        підприємства з виробництва скла та крохмалю, страхова компанія та Міжнародний інвестиційний банк.</p>
    <p>Під час передвиборчої президентської кампанії Порошенко обіцяв, ставши президентом, продати всі активи
        (окрім
        «5 каналу»), включно з групою «Рошен», Міжнародним інвестиційним банком (9,92 % особисто і 50,08 % через
        його ж Прайм Ессетс Кепітал). Активи останнього з початку 2014 року виросли майже на 50 % до 2,5 млрд
        грн.</p>
    <p>Але цього не сталося. Після виборів Порошенко передав активи в управління інвестиційному фондові Прайм
        Ессетс Кепітал, який належить самому Порошенкові, а формально ним керує його батько Після цього вказаний
        фонд купив ПАТ «Київський завод експериментальних конструкцій»</p>

    <p>13 березня 2015 року, Порошенко заявив про залучення до продажу активів компанії Rothschild, з якою було
        укладено угоду, згідно з якою він не має права втручатися в процес продажу, бо вони отримали всі
        повноваження. За ствердженням представників «Рошену», 24 квітня 2015 року слідчий комітет РФ наклав
        арешт на майно ВАТ «Липецька кондитерська фабрика „Рошен“». Компанію позбавили права розпоряджатися цим
        майном до 13 вересня 2015 року. Рошен стверджує, що через це Порошенко так і не продав указану
        фабрику. На початку 2017 року топменеджмент «Рошена» прийняв рішення закрити Липецьку кондитерську
        фабрику. 1 квітня 2017 року вона припинила свою роботу. 19 червня 2017 року завершено консервацію
        Липецької фабрики «Рошен</p>
    <p>3 серпня Порошенко заявив, що його бізнесом керують західні інвестиційні компанії, пояснюючи, що для
        заможних
        людей нормально займатись політикою.<br>
        14 січня 2016 року Порошенко заявив, що передав свою частку «Рошену» в незалежний сліпий траст і не може
        контролювати актив на період президентської каденції, надалі пообіцяв передати трасту «Міжнародний
        інвестиційний банк». Наступного дня посол США в Україні Джеффрі Пайєтт оцінив це як «важливий та
        необхідний крок».<br>

        У січні 2020 року стало відомо, що основний бізнес Петро Порошенко переписав на старшого сина
        Олексія.<br>

        У лютому 2021 року Петро Порошенко оголосив, що став кінцевим бенефіціарним власником телеканалу
        «Прямий».<br>
