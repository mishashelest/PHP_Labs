<p>Окрім політичної діяльності, Порошенко займається підприємництвом, регулярно займаючи місця у рейтингах
    найзаможніших українців, частково володіє кондитерською корпорацією «Рошен», кількома автозаводами,
    інформаційними П'ятим каналом та каналом Прямий.</p>
<p>Кандидат юридичних наук (2002, Одеська юридична академія). Лауреат Премії України в галузі науки й техніки,
    заслужений економіст України.</p>
