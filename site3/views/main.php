<p><b>Петро Порошенко</b> (нар. 26 вересня 1965; Болград) — український політик та підприємець; п'ятий президент
    України (7 червня 2014 — 20 травня 2019). Народний депутат України,
    керівник партії «Європейська солідарність».</p>
<p>Народний депутат III, IV, V, VII та IX скл. (1998—2014, із 2019). Засновник «Партії солідарності України»,
    один зі співзасновників партії «Трудова солідарність України»,
    що згодом змінила назву на Партія Регіонів.
    Працював міністром закордонних справ (2009—2010) в другому уряді Юлії Тимошенко і міністром торгівлі (2012)
    в уряді Миколи Азарова.
    Був членом Ради Національного банку України (2007—2014). Один зі лідерів Помаранчевої Революції.</p>

<p>Обраний на пост президента України у травні 2014 року, здобувши 54,7 % голосів у першому турі.
    За час президентства призупинив активну російську агресію на сході України, добився зовнішньополітичної
    підтримки України державами Заходу.
    При ньому Україна отримала безвізовий режим з ЄС (2017). Сприяв наданню автокефалії Українській православній
    церкві (2018—2019). Ініціював реформи в армії, освіті, медицині, сферах державного управління тощо. Після
    Чорноморського інциденту тимчасово запровадив воєнний стан (2018).
    Брав участь у президентських виборах 2019 року, але програв продюсеру і шоумену Володимиру Зеленському у
    другому турі.</p>
