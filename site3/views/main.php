    <p><b>Політика</b> 1998 року Порошенко став членом фракції СДПУ(о) у ВРУ та був в її політбюро[35]. На
        виборах
        мав № 11 у списку. Однак мандат здобув за мажоритарним округом № 12[36]. На початку 2000 року залишив цю
        партію.У травні 2000 року створив фракцію Солідарність, згодом — однойменну партію. Остання восени того
        року
        ввійшла до Партії регіонального відродження «Трудова солідарність України» (згодом — Партія регіонів).
        Порошенко став співголовою утвореної партії, у березні 2001 року — заступником голови, через пів року
        залишив партію. <br>
        У грудні 2001 року, партія «Солідарність» увійшла у виборчий блок Ющенка «Наша Україна», а Порошенко
        став
        керівником виборчого штабу. Після парламентських виборів 2002 року опозиційний блок Ющенка «Наша
        Україна»
        вперше одержав понад 25 % голосів виборців. За списком «Нашої України» Порошенко у 2002 і 2006 роках
        обирався народним депутатом. Очолював парламентські комітети з питань бюджету (2002—2005) та фінансів
        (2006—2007). <br>
        З липня 2004 — заступник керівника штабу коаліції «Сила народу», з моменту створення партії Наша Україна
        — в
        її керівництві. <br>
        8 лютого 2005 — призначений на посаду секретаря РНБО. 5 вересня 2005 року після висунення проти нього
        звинувачень з боку колишнього держсекретаря Зінченка подав заяву про відставку. Тоді Зінченко звинуватив
        Порошенка в корупції та вимагав від нього відокремлення бізнесу від влади[37] Проти Порошенка відкрили
        кримінальну справу, але її було закрито. <br>
        З 22 лютого 2007 року, Порошенко — голова Ради НБУ.</p>
