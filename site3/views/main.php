        закордонних
        справ, за це проголосували 240 депутатів. На цій посаді Порошенко працював до 11 березня 2010 року, коли
        був
        звільнений разом з іншими міністрами, тодішнім президентом Януковичем. <br>
        23 грудня 2009 — обраний почесним професором Університету банківської справи НБУ. <br>
        23 березня 2012 — Президент Віктор Янукович призначив Порошенка міністром економічного розвитку і
        торгівлі
        України[39]. На цій посаді працював до 3 грудня 2012, звільнений Указом Президента України №
        679/2012.<br>
        На виборах до ВРУ 2012 року був кандидатом у народні депутати в окрузі № 12[41]. З грудня 2012 року —
        нардеп
        України 7-го скликання, до парламенту пройшов самовисуванцем. Був позафракційним членом Комітету з
        питань
        європейської інтеграції. <br>
        У лютому 2013 року Петро Порошенко заявив, що може взяти участь у виборах мера Києва, як єдиний кандидат
        від
        опозиції, якщо його підтримають[42] Згодом висловлював готовність підтримати кандидатуру Віталія Кличка.
        На
        березень 2013 року на виборах мера Києва за Порошенка були готові проголосувати 7 % киян, за Віталія
        Кличка
        — 31 %, за Попова — 20,3 %[43]. <br>
        Під час сутичок на Банковій 1 грудня 2013 року, Порошенко разом із сином вийшов на захист мирних
        демонстрантів від міліції та провокаторів[44]. У грудні в перший місяць Євромайдану президентський
