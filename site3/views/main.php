    <p><b>Дитинство</b> Народився 26 вересня 1965 року в Болграді Одеської області. Батько Олексій Порошенко
        (1936—2020) на той час працював з міською сільгосптехнікою, мати Порошенко Євгенія Сергіївна (1937—2004)
        —
        головним бухгалтером в управлінні сільського господарства.Згодом родина переїхала до молдовського міста
        Бендери. У школі Порошенко займався дзюдо, здобував нагороди на змаганнях; кандидат у майстри спорту У
        Бендерах закінчив середню школу.</p>
    <p>З 1984 по 1986 рік служив у Радянській армії.</p>
