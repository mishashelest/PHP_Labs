<div class="maintext">

    <p><b>Освіта</b> У 1989 році, після семи років навчання, з відзнакою закінчив факультет міжнародних відносин
        та
        міжнародного права КНУ ім. Шевченка за спеціальністю міжнародні економічні відносини, продовжив навчання
        в
        аспірантурі. До 1992 року навчався в аспірантурі та працював асистентом кафедри міжнародних економічних
        відносин Київського університету.</p>
    <p>У 2002 році захистив в Одеській юридичній академії кандидатську дисертацію на тему «Правове регулювання
        управління державними корпоративними правами в Україні»Автор монографій «Державне управління
        корпоративними
        правами в Україні. Теорія формування правовідносин» та низки наукових публікацій. Співавтор підручника
        «Сучасні міжнародні економічні відносини».Володіє українською, російською, англійською й румунською
        (молдовською) мовами.</p>
